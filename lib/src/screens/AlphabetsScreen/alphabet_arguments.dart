  final String title;
  final String message;
  final String soundLetter;
  final String image1;
  final String image1Title;
  final String image2;
  final String image2Title;
  final String image3;
  final String image3Title;
  final String image4;
  final String image4Title;

  AlphabetArguments(
      this.title,
      this.message,
      this.soundLetter,
      this.image1,
      this.image1Title,
      this.image2,
      this.image2Title,
      this.image3,
      this.image3Title,
      this.image4,
      this.image4Title);
}
