// You can pass any object to the arguments parameter.
// In this example, create a class that contains both
// a customizable title and message.
class ScreenArguments {
  final String title;
  final String message;
  final String soundLetter;
  final String image1;
  final String image2;
  final String image3;
  final String image4;

  ScreenArguments(this.title, this.message, this.soundLetter, this.image1,
      this.image2, this.image3, this.image4);
}
